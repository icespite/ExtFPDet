    var host = window.location.host;
    var parts = host.split(".");
    var hostPartsLen = parts.length;
    var mainHost = ""
    for(var i=0; i<hostPartsLen;i++){
        if (i == 0){
            mainHost = parts[i]
        }
        else if(i== 1){
            continue;
        }else {
            mainHost = mainHost + "." + parts[i];
        }
    }

    var processATag = function(node, index){
        var href = node.href;
        if (href.match(mainHost)) {
            node.href = href.replace(mainHost, host);
        }
    }

    var processFormTag = function(node, index){
        var action = node.action;
        if (action.match(mainHost)) {
            node.action = action.replace(mainHost, host);
        }
    }
